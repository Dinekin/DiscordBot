const { SlashCommandBuilder, PermissionFlagsBits, EmbedBuilder } = require('discord.js');
const MessageLog = require('../models/MessageLog');
const Guild = require('../models/Guild');
const logger = require('../utils/logger');

module.exports = {
  data: new SlashCommandBuilder()
    .setName('messagelog')
    .setDescription('ZarzƒÖdzaj lub przeglƒÖdaj logi wiadomo≈õci')
    .setDefaultMemberPermissions(
      PermissionFlagsBits.Administrator | 
      PermissionFlagsBits.ManageGuild | 
      PermissionFlagsBits.ManageRoles | 
      PermissionFlagsBits.ManageMessages
    )
    .addSubcommand(subcommand =>
      subcommand
        .setName('search')
        .setDescription('Wyszukaj wiadomo≈õci u≈ºytkownika')
        .addUserOption(option =>
          option.setName('user')
            .setDescription('U≈ºytkownik, kt√≥rego wiadomo≈õci chcesz znale≈∫ƒá')
            .setRequired(true))
        .addIntegerOption(option =>
          option.setName('limit')
            .setDescription('Liczba wiadomo≈õci do pobrania (maksymalnie 10)')
            .setMinValue(1)
            .setMaxValue(10))
        .addBooleanOption(option =>
          option.setName('deleted')
            .setDescription('Czy pokazaƒá tylko usuniƒôte wiadomo≈õci')))
    .addSubcommand(subcommand =>
      subcommand
        .setName('channel')
        .setDescription('Ustaw kana≈Ç logowania wiadomo≈õci')
        .addChannelOption(option =>
          option.setName('channel')
            .setDescription('Kana≈Ç do logowania wiadomo≈õci')
            .setRequired(true)))
    .addSubcommand(subcommand =>
      subcommand
        .setName('toggle')
        .setDescription('W≈ÇƒÖcz lub wy≈ÇƒÖcz logowanie wiadomo≈õci')
        .addBooleanOption(option =>
          option.setName('enabled')
            .setDescription('Czy logowanie wiadomo≈õci jest w≈ÇƒÖczone')
            .setRequired(true)))
    .addSubcommand(subcommand =>
      subcommand
        .setName('exclude')
        .setDescription('Dodaj kana≈Ç do listy wykluczonych z logowania')
        .addChannelOption(option =>
          option.setName('channel')
            .setDescription('Kana≈Ç do wykluczenia z logowania')
            .setRequired(true)))
    .addSubcommand(subcommand =>
      subcommand
        .setName('include')
        .setDescription('Usu≈Ñ kana≈Ç z listy wykluczonych')
        .addChannelOption(option =>
          option.setName('channel')
            .setDescription('Kana≈Ç do przywr√≥cenia logowania')
            .setRequired(true)))
    .addSubcommand(subcommand =>
      subcommand
        .setName('excluded')
        .setDescription('Wy≈õwietl listƒô wykluczonych kana≈Ç√≥w')),
  
  async execute(interaction) {
    logger.info(`Rozpoczƒôto wykonywanie komendy messagelog przez ${interaction.user.tag}`);
    
    const subcommand = interaction.options.getSubcommand();
    logger.debug(`Podkomenda messagelog: ${subcommand}`);
    
    // Pobierz ustawienia serwera
    let guildSettings = await Guild.findOne({ guildId: interaction.guildId });
    
    // Je≈õli nie ma, utw√≥rz nowe
    if (!guildSettings) {
      logger.debug(`Nie znaleziono ustawie≈Ñ dla serwera ${interaction.guildId}, tworzenie nowych`);
      guildSettings = await Guild.create({
        guildId: interaction.guildId,
        modules: {
          messageLog: false
        }
      });
    }
    
    if (subcommand === 'search') {
      logger.debug(`Wykonywanie podkomendy search`);
      const user = interaction.options.getUser('user');
      const limit = interaction.options.getInteger('limit') || 5;
      const showDeleted = interaction.options.getBoolean('deleted') || false;
      
      await interaction.deferReply({ ephemeral: true });
      
      // Stw√≥rz filtr wyszukiwania
      const filter = { 
        guildId: interaction.guildId, 
        authorId: user.id 
      };
      
      // Je≈õli ma pokazaƒá tylko usuniƒôte, dodaj warunek
      if (showDeleted) {
        filter.deletedAt = { $ne: null };
      }
      
      logger.debug(`Filtr wyszukiwania: ${JSON.stringify(filter)}`);
      
      try {
        // Pobierz logi wiadomo≈õci
        const logs = await MessageLog.find(filter)
          .sort({ createdAt: -1 })
          .limit(limit);
          
        logger.debug(`Znaleziono ${logs.length} wiadomo≈õci`);
      
        if (logs.length === 0) {
          return interaction.editReply({
            content: `Nie znaleziono ≈ºadnych wiadomo≈õci ${showDeleted ? 'usuniƒôtych ' : ''}u≈ºytkownika ${user.tag}.`,
            ephemeral: true
          });
        }
        
        // Stw√≥rz embedded z wynikami
        const embed = new EmbedBuilder()
          .setTitle(`Logi wiadomo≈õci: ${user.tag}`)
          .setColor(showDeleted ? '#e74c3c' : '#3498db')
          .setThumbnail(user.displayAvatarURL({ dynamic: true }))
          .setFooter({ text: `ID u≈ºytkownika: ${user.id}` })
          .setTimestamp();
        
        // Dodaj wiadomo≈õci do embedu
        logs.forEach((log, index) => {
          const messageDate = `<t:${Math.floor(log.createdAt.getTime() / 1000)}:R>`;
          const channelInfo = `<#${log.channelId}>`;
          const status = log.deletedAt ? 'üóëÔ∏è Usuniƒôta' : log.editedAt ? '‚úèÔ∏è Edytowana' : 'üìù Wys≈Çana';
          
          let content = log.content ? log.content.substring(0, 512) : '*Brak tre≈õci*';
          if (log.content && log.content.length > 512) {
            content += '... (skr√≥cono)';
          }
          
          let attachmentText = '';
          if (log.attachments && log.attachments.length > 0) {
            attachmentText = '\nüìé ' + log.attachments.map(a => a.name).join(', ');
          }
          
          embed.addFields({
            name: `${status} ${messageDate} w ${channelInfo}`,
            value: content + attachmentText
          });
        });
        
        await interaction.editReply({
          embeds: [embed],
          ephemeral: true
        });
      } catch (error) {
        logger.error(`B≈ÇƒÖd podczas wyszukiwania wiadomo≈õci: ${error.stack}`);
        return interaction.editReply({
          content: `WystƒÖpi≈Ç b≈ÇƒÖd podczas wyszukiwania wiadomo≈õci: ${error.message}`,
          ephemeral: true
        });
      }
    }
    
    else if (subcommand === 'channel') {
      logger.debug(`Wykonywanie podkomendy channel`);
      const channel = interaction.options.getChannel('channel');
      
      // Sprawd≈∫ typ kana≈Çu
      if (channel.type !== 0) { // 0 = kana≈Ç tekstowy
        logger.warn(`U≈ºytkownik pr√≥bowa≈Ç ustawiƒá kana≈Ç typu ${channel.type} jako kana≈Ç log√≥w`);
        return interaction.reply({
          content: 'Musisz wybraƒá kana≈Ç tekstowy!',
          ephemeral: true
        });
      }
      
      try {
        // Zaktualizuj ustawienia
        guildSettings.messageLogChannel = channel.id;
        await guildSettings.save();
        
        logger.info(`Kana≈Ç logowania wiadomo≈õci dla serwera ${interaction.guildId} ustawiony na ${channel.id}`);
        
        await interaction.reply({
          content: `Ustawiono kana≈Ç logowania wiadomo≈õci na ${channel}.`,
          ephemeral: true
        });
      } catch (error) {
        logger.error(`B≈ÇƒÖd podczas ustawiania kana≈Çu log√≥w: ${error.stack}`);
        return interaction.reply({
          content: `WystƒÖpi≈Ç b≈ÇƒÖd podczas ustawiania kana≈Çu log√≥w: ${error.message}`,
          ephemeral: true
        });
      }
    }
    
    else if (subcommand === 'toggle') {
      logger.debug(`Wykonywanie podkomendy toggle`);
      const enabled = interaction.options.getBoolean('enabled');
      
      try {
        // Upewnij siƒô, ≈ºe modu≈Çy istniejƒÖ
        if (!guildSettings.modules) {
          guildSettings.modules = {};
        }
        
        guildSettings.modules.messageLog = enabled;
        await guildSettings.save();
        
        logger.info(`Logowanie wiadomo≈õci dla serwera ${interaction.guildId} zosta≈Ço ${enabled ? 'w≈ÇƒÖczone' : 'wy≈ÇƒÖczone'}`);
        
        await interaction.reply({
          content: `Logowanie wiadomo≈õci zosta≈Ço ${enabled ? 'w≈ÇƒÖczone' : 'wy≈ÇƒÖczone'}.`,
          ephemeral: true
        });
      } catch (error) {
        logger.error(`B≈ÇƒÖd podczas prze≈ÇƒÖczania logowania wiadomo≈õci: ${error.stack}`);
        return interaction.reply({
          content: `WystƒÖpi≈Ç b≈ÇƒÖd podczas zmiany ustawie≈Ñ logowania wiadomo≈õci: ${error.message}`,
          ephemeral: true
        });
      }
    }
    
    else if (subcommand === 'exclude') {
      logger.debug(`Wykonywanie podkomendy exclude`);
      const channel = interaction.options.getChannel('channel');
      
      // Sprawd≈∫ typ kana≈Çu
      if (channel.type !== 0) { // 0 = kana≈Ç tekstowy
        logger.warn(`U≈ºytkownik pr√≥bowa≈Ç wykluczyƒá kana≈Ç typu ${channel.type}`);
        return interaction.reply({
          content: 'Mo≈ºna wykluczyƒá tylko kana≈Çy tekstowe!',
          ephemeral: true
        });
      }
      
      try {
        // Sprawd≈∫ czy kana≈Ç ju≈º jest wykluczony
        if (guildSettings.excludedChannels && guildSettings.excludedChannels.includes(channel.id)) {
          return interaction.reply({
            content: `Kana≈Ç ${channel} jest ju≈º wykluczony z logowania.`,
            ephemeral: true
          });
        }
        
        // Dodaj kana≈Ç do listy wykluczonych
        if (!guildSettings.excludedChannels) {
          guildSettings.excludedChannels = [];
        }
        guildSettings.excludedChannels.push(channel.id);
        await guildSettings.save();
        
        logger.info(`Kana≈Ç ${channel.id} dodany do wykluczonych na serwerze ${interaction.guildId}`);
        
        await interaction.reply({
          content: `‚úÖ Kana≈Ç ${channel} zosta≈Ç wykluczony z logowania wiadomo≈õci.`,
          ephemeral: true
        });
      } catch (error) {
        logger.error(`B≈ÇƒÖd podczas wykluczania kana≈Çu: ${error.stack}`);
        return interaction.reply({
          content: `WystƒÖpi≈Ç b≈ÇƒÖd podczas wykluczania kana≈Çu: ${error.message}`,
          ephemeral: true
        });
      }
    }
    
    else if (subcommand === 'include') {
      logger.debug(`Wykonywanie podkomendy include`);
      const channel = interaction.options.getChannel('channel');
      
      try {
        // Sprawd≈∫ czy kana≈Ç jest wykluczony
        if (!guildSettings.excludedChannels || !guildSettings.excludedChannels.includes(channel.id)) {
          return interaction.reply({
            content: `Kana≈Ç ${channel} nie jest wykluczony z logowania.`,
            ephemeral: true
          });
        }
        
        // Usu≈Ñ kana≈Ç z listy wykluczonych
        guildSettings.excludedChannels = guildSettings.excludedChannels.filter(id => id !== channel.id);
        await guildSettings.save();
        
        logger.info(`Kana≈Ç ${channel.id} usuniƒôty z wykluczonych na serwerze ${interaction.guildId}`);
        
        await interaction.reply({
          content: `‚úÖ Przywr√≥cono logowanie wiadomo≈õci dla kana≈Çu ${channel}.`,
          ephemeral: true
        });
      } catch (error) {
        logger.error(`B≈ÇƒÖd podczas przywracania logowania kana≈Çu: ${error.stack}`);
        return interaction.reply({
          content: `WystƒÖpi≈Ç b≈ÇƒÖd podczas przywracania logowania kana≈Çu: ${error.message}`,
          ephemeral: true
        });
      }
    }
    
    else if (subcommand === 'excluded') {
      logger.debug(`Wykonywanie podkomendy excluded`);
      
      try {
        if (!guildSettings.excludedChannels || guildSettings.excludedChannels.length === 0) {
          return interaction.reply({
            content: 'Nie ma ≈ºadnych wykluczonych kana≈Ç√≥w.',
            ephemeral: true
          });
        }
        
        // Przygotuj listƒô wykluczonych kana≈Ç√≥w
        const excludedList = [];
        for (const channelId of guildSettings.excludedChannels) {
          try {
            const channel = await interaction.guild.channels.fetch(channelId);
            if (channel) {
              excludedList.push(`‚Ä¢ <#${channelId}> (${channel.name})`);
            } else {
              excludedList.push(`‚Ä¢ NieistniejƒÖcy kana≈Ç (${channelId})`);
            }
          } catch (fetchError) {
            excludedList.push(`‚Ä¢ NieistniejƒÖcy kana≈Ç (${channelId})`);
          }
        }
        
        const embed = new EmbedBuilder()
          .setColor(0x3498db)
          .setTitle('üö´ Wykluczone kana≈Çy z logowania')
          .setDescription(excludedList.join('\n'))
          .setFooter({ text: `≈ÅƒÖcznie wykluczonych: ${guildSettings.excludedChannels.length}` })
          .setTimestamp();
        
        await interaction.reply({
          embeds: [embed],
          ephemeral: true
        });
      } catch (error) {
        logger.error(`B≈ÇƒÖd podczas wy≈õwietlania wykluczonych kana≈Ç√≥w: ${error.stack}`);
        return interaction.reply({
          content: `WystƒÖpi≈Ç b≈ÇƒÖd podczas wy≈õwietlania wykluczonych kana≈Ç√≥w: ${error.message}`,
          ephemeral: true
        });
      }
    }
  },
};